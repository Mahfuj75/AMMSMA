package asa.org.bd.ammsma.extra;

public class SavingsFriendly {

    private int savingFriendlyLoanCount;
    private int primaryLoanCount;
    private float maxWithdrawal;

    public int getSavingFriendlyLoanCount() {
        return savingFriendlyLoanCount;
    }

    public void setSavingFriendlyLoanCount(int savingFriendlyLoanCount) {
        this.savingFriendlyLoanCount = savingFriendlyLoanCount;
    }

    public int getPrimaryLoanCount() {
        return primaryLoanCount;
    }

    public void setPrimaryLoanCount(int primaryLoanCount) {
        this.primaryLoanCount = primaryLoanCount;
    }

    public float getMaxWithdrawal() {
        return maxWithdrawal;
    }

    public void setMaxWithdrawal(float maxWithdrawal) {
        this.maxWithdrawal = maxWithdrawal;
    }
}
