package asa.org.bd.ammsma.extra;


public class CalenderData {
    private int dateInteger;
    private String dateString;
    private int dayInteger;


    public int getDateInteger() {
        return dateInteger;
    }

    public void setDateInteger(int dateInteger) {
        this.dateInteger = dateInteger;
    }

    public String getDateString() {
        return dateString;
    }

    public void setDateString(String dateString) {
        this.dateString = dateString;
    }

    public int getDayInteger() {
        return dayInteger;
    }

    public void setDayInteger(int dayInteger) {
        this.dayInteger = dayInteger;
    }
}
